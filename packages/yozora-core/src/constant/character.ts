/**
 * 字符编码
 */
export enum CharCode {
  /**
   * 制表符 '\t'
   */
  TAB = 9,
  /**
   * 换行符 '\n'
   */
  LINE_FEED = 10,
  /**
   * 线制表符
   */
  LINE_TABULATION = 11,
  /**
   * 换页符
   */
  FORM_FEED = 12,
  /**
   * 回车 '\r'
   */
  CARRIAGE_RETURN = 13,
  /**
   * 空白符 ' '
   */
  SPACE = 32,
  /**
   * 美元符号 '$'
   */
  DOLLAR = 36,
  /**
   * 反斜杠 '\\'
   */
  BACK_SLASH = 92,
  /**
   * 反引号 '`'
   */
  BACKTICK = 96,
  /**
   * 波浪号 `~`
   */
  TILDE = 126,
}
